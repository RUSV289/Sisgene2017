public class Campoportada {
    private String cap_id;
    private String sec_id;
    private String sus_id;
    private String cae_id;
    private String cap_numero;
    private String cap_descripcion;

    public Campoportada() {
    }

    public String getCap_id() {
        return cap_id;
    }

    public void setCap_id(String cap_id) {
        this.cap_id = cap_id;
    }

    public String getSec_id() {
        return sec_id;
    }

    public void setSec_id(String sec_id) {
        this.sec_id = sec_id;
    }

    public String getSus_id() {
        return sus_id;
    }

    public void setSus_id(String sus_id) {
        this.sus_id = sus_id;
    }

    public String getCae_id() {
        return cae_id;
    }

    public void setCae_id(String cae_id) {
        this.cae_id = cae_id;
    }

    public String getCap_numero() {
        return cap_numero;
    }

    public void setCap_numero(String cap_numero) {
        this.cap_numero = cap_numero;
    }

    public String getCap_descripcion() {
        return cap_descripcion;
    }

    public void setCap_descripcion(String cap_descripcion) {
        this.cap_descripcion = cap_descripcion;
    }
}
